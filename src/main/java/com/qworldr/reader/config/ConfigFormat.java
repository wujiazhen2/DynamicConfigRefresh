package com.qworldr.reader.config;

public class ConfigFormat {
    private String suffix;
    private String path;

    public String getSuffix() {
        return suffix;
    }

    public String getPath() {
        return path;
    }

    public void setSuffix(String suffix) {
        this.suffix = suffix;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
